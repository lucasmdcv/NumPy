#O NumPy é uma biblioteca fundamental para a computação científica com Python. Ela permite a realização de operações matemáticas e lógicas em grandes arrays e matrizes multidimensionais. Aqui estão algumas razões pelas quais o NumPy é amplamente utilizado:
#Eficiência: O NumPy é escrito em C, o que torna as operações com arrays muito mais rápidas do que as listas padrão do Python.
#Funcionalidades: Oferece uma vasta gama de funções matemáticas e estatísticas, facilitando o trabalho com dados científicos.
#Integração: Funciona bem com outras bibliotecas como SciPy, pandas, e Matplotlib, tornando-o ideal para projetos de ciência de dados e aprendizado de máquina.
#Simplicidade: A sintaxe do NumPy é clara e concisa, permitindo a execução de operações complexas com poucas linhas de código.

import numpy as np

# Criar um array de 1 dimensão
array = np.array([1, 2, 3, 4, 5])

# Criar um array de 2 dimensões (matriz)
matriz = np.array([[1, 2, 3], [4, 5, 6]])

print(array)
print(matriz)



